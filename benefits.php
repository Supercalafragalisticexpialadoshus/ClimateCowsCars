			<div id="efficiency" class="strong"><u><b>Efficiency</b></u></div>
			<div class="image left" style="width:350px; margin-bottom:.7em;">
			<img src="images/benefits/transport-effic-compare.jpg" style="width:100%;"></a>
			<br><p style="margin:0;padding:0;">Data: Sustainable Transport and Public Policy,
			<a href="http://users.ox.ac.uk/~geog0213/" target="_mozilla">David Banister,</a> Univ. of Oxford</p></div>
			<div class="textblock">
				<p>Another huge advantage to both lifestyles is
efficiency. Both in terms of space (see below)
and energy needed. It might not suprise you
to learn that automobiles are the least efficient
machines that we have for moving us around.
A car has an efficiency of <a href="http://www.nature.com/nature/journal/v488/n7411/fig_tab/nature11475_F2.html" target="_mozilla">roughly 20%</a> from oil
well to passenger kilometer (or mile). On top
of that, less then 1% of the energy used by a car
is actually needed to carry a person. (<a href="https://www.wilsoncenter.org/sites/default/files/Lovins.pdf" target="_mozilla">link</a>) By
contrast, a human on a bicycle has an efficiency
Data: Sustainable Transport and Public Policy,
estimated between <a href="https://en.wikipedia.org/wiki/Energy_efficiency_in_transportation" target="_mozilla">700</a> and 
<a href="http://auto.howstuffworks.com/question527.htm" target="_mozilla">1000</a> miles per
David Banister , Univ. of Oxford
gallon. With a bicycle, our food is our fuel,
which means that we can eat (within reason) anything that we want.</p>
