			<div id="separator"></div>
			<div id="social-connections" class="strong"><u><b>Social Connections</b></u></div>
			<blockquote style="margin:1em auto; width: 90%;">Cities are books that you read with your feet.”<br> 
			<i>Quintin Cabrera</i></blockquote>
			<div class="image right"><img src="images/benefits/streetparty-potluck.png" width="350px" height="auto"></div>
			<div class="textblock">
				<p>But there’s more to it then that. As just one
example, every 10 minutes commuting without a
car results in 10% more social connections. (<a href="http://www.newyorker.com/magazine/2007/04/16/there-and-back-again"
target="_mozilla">link</a>)
These social connections provide enormous benefit
to our well-being. They fight depression, <a href="http://www.vancourier.com/opinion/isolation-bad-for-democracy-1.381258"
target="_mozilla">improve
democracy,</a> and increase sharing. (<a href="http://www.strongtowns.org/journal/2016/2/12/biking-is-a-gateway-drug-to-social-awareness"
target="_mozilla">link</a>) (<a href="http://bewell.stanford.edu/features/social-ties-good-health"
target="_mozilla">link</a>) <br>
Seeing the street as a place rather then a through-
way, helps foster a greater sense of community
and connection within our neighbourhoods. (<a href="http://www.pps.org/reference/streets-as-places-how-transportation-can-create-a-sense-of-community/"
target="_mozilla">link</a>)
(<a href="http://www.theguardian.com/cities/2015/jun/09/cities-physically-active-residents-more-productive-healthier-walking-cycling-economic-benefits?utm_content=buffer6fd31&utm_medium=social&utm_source=twitter.com&utm_campaign=buffer"
target="_mozilla">link</a>) When people travel by foot or by cycle, we
get to enjoy spur-of-the moment conversations, we might ask someone on a date, or
see a rainbow. I’ve had a beautiful wealth of experiences that I often realize would be
impossible were I traveling alone within an enclosed vehicle.</p>
			</div><!-- Textblock -->
			<blockquote style="margin:1em auto; width: 90%;">When you ride a bicycle, you’re part of a neighbourhood.<br> 
			When you’re in a car, your just passing through.”<br>
			<i><a href="http://www.tarasgrescoe.com/straphanger/contact.html" target="_mozilla">Taras Grescoe</a></i></blockquote>

			<div class="image left" style="width:350px; margin-bottom:.7em;">
			<img src="images/benefits/commute-happiness.png" style="width:100%;"></a>
			<br><p style="margin:0;padding:0;">Commute Well-Being study by Oliver Smith PhD.
Portland State University</p></div>
			<div class="textblock">
				<p>This improved connection to our
neighbourhood plays a big part in our overall
happiness not only while we’re travelling, but
after we reach our destination too. (<a href="http://phys.org/news/2014-05-mode-affects.html"
target="_mozilla">link</a>) The
fresh air, the relaxed pace and the freedom
from traffic all play a large part in lowering
stress levels. There’s sound research to support
this. (<a href="https://momentummag.com/mental-health-benefits-of-cycling/"
target="_mozilla">link</a>)
Commute Well-Being study by Oliver Smith PhD.
Portland State University
According to Dr. Oliver Smith at Portland State
University, bike commuters were found to have
the highest “happiness quotient” of all transportation modes. 
(<a href="http://bikeportland.org/wp-content/uploads/2013/01/TRB_Osmith_55x44.pdf"
target="_mozilla">link</a>) Smith surveyed over
800 commuters using various modes about their level of satisfaction.</p>
