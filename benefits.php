			<div class="textblock">
				<p>On top of the efficiency of space and fuel, bikes and walking also cause less damage to
the road itself. I’m sure you can think back to the many times you were inconvenienced
by having to wait for, or detour around a road resurfacing project. Well these are more
then just inconvenient. They cost A LOT of money. Resurfacing an arterial road costs
roughly $1 million per mile. (<a href="http://www.dot.state.fl.us/planning/policy/costs/costs-D7.pdf" target="_mozilla">link</a>)
Compared to cars and trucks, a bicycle causes an infinitely small amount of damage.
Using an ‘average’ 4000lb car as the baseline you can see the huge difference.</p>

