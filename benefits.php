				<div id="separator"><h3>Benefits</h3></div>
				<!-- https://developer.mozilla.org/en-US/Learn/HTML/Multimedia_and_embedding/Video_and_audio_content custom video embed -->
				<div id="video-area" style="width:350px;">
					<video controls width="350px" height="291px" poster="images/benefits/Vegan-nugget-screenshot.png">
