			<div class="textblock">
				<iframe class="image right" width="350" height="250" src="https://www.youtube.com/embed/kIKN5LNxFiQ" frameborder="0" allowfullscreen></iframe>
				<p>What has always impressed me is the youthful
shine that shows on the faces of people who
eat healthy and stay active. I’ll never forget a
trip that I took years ago with a young couple,
climbing through a small mountain area. I felt a
bit of struggle trying to keep up with these young
20 year olds and felt my own age as we climbed
the hills. It was only a year later when I ran into
one of them again that I found that he was in fact 30 years old. Since then I’ve noticed
that vegan eaters and active cyclists look, so much younger then other people of the
same age. (<a href="http://www.huffingtonpost.com/2013/03/24/vegan-diet-help-skin_n_2933134.html"
target="_mozilla">link</a>) <div id="ref">('World Peace Diet' pg-198)</div></p>
			</div> <!-- textblock -->
			
