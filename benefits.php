				<div class="textblock">
					<p id="first">There are a huge number of advantages that come
with both <a href="http://www.onegreenplanet.org/natural-health/health-perks-of-going-vegan/" target="_mozilla">a vegan diet</a> 
and a <a href="http://www.alternet.org/story/146422/no_car,_no_problem%3A_the_benefits_of_car-free_living" target="_mozilla">
car-free lifestyle.</a>
				Both choices help us to enjoy longer, more stress-free
lives while reducing our impact on the land. A carfree lifestyle that includes body movement improves not only the muscles
but also improves concentration, social connections, and saves money. 
(<a href="http://www.velo.qc.ca/files/file/TA/VQ_cycling_infographic.pdf" target="_mozilla">link</a>)<br>  
A vegan lifestyle improves energy levels, nutrition, heart health,
and our immune system. In fact there are so many
advantages that they would easily overwhelm this
essay. You can save time by just watching 
<a href="http://www.forksoverknives.com/the-film/" target="_mozilla">‘Forks Over
Knives’</a> for a quick overview. Or, you can read <a href="http://freefromharm.org/health-nutrition/catching-up-with-science-burying-the-humans-need-meat-argument/" target="_mozilla">this wonderful article</a> on the demise of the 'humans need meat' argument, which provides worldwide scientific sources.<br></p>
				<p>Eating plant-based also prevents the ‘dulling of the senses’ caused by overloading the
taste buds. People who switch to plant-based meals find that flavours are richer, more
varied, and more delicious as the taste-buds heal. <div id="ref">('World Peace Diet' pg-81)</div><br>
And while Emily’s video on the right is used mainly to give you a smile, it does feel in my
own opinion, that eating vegan really has the potential to improve our lives in a huge
number of ways. Both <a href="https://www.youtube.com/watch?v=ldGP6TjYtJw" target="_mozilla">common</a>, 
and <a href="http://eatdrinkbetter.com/2014/06/28/5-unusual-benefits-going-vegan-warning-poop-talk/" target="_mozilla">unexpected</a>.</p> 
