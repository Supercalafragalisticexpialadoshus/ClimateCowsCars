			<div class="textblock">
				<p>Compared to a plant-based diet however,
the health benefits of living car-free are
less well known (<a href="http://www.modacitylife.com/cycle-chic-films/" target="_mozilla">not as many Netflix
movies about it</a>). Clearly the increased
exercise of walking, bicycling or even
using public transit (which involves
walking) will improve our lives. (<a href="http://www.treehugger.com/health/study-cycling-keeps-you-young.html"
target="_mozilla">link</a>)
The exercise boosts ones mood, improves connection with neighbourhoods, and
provides many other benefits.<br>
</i>(I cover this in more detail below.)</i></p>
