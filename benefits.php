			<div class="textblock">
				<p>The efficiency of a vegan diet is mainly linked to the
huge reduction in the amount of <a href="http://iopscience.iop.org/1748-9326/8/3/034015" target="_mozila">land and food</a> used
when raising animals to adulthood. Whether an animal
is eating grass or corn, the amount of food needed to
raise a cow vastly outweighs (no pun intended) the
yield. In the United States, cattle will eat over 8.5
metric tonnes of food per year. Since they’re killed
after 18 months, that’s about 13 tonnes of food needed
per animal. And the return that a rancher gets is
about 254 kilograms (560 lbs) of saleable meat. With a
vegan diet on the other hand, the food goes directly to
people where it gets turned into human energy. This
means that more people can be fed on the same amount of grain, oats, rice, or potatoes.</p>
