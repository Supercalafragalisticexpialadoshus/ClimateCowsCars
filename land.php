				<div id="separator"><h3>Land</h3></div>
				<div class="textblock">
				<div class="image right" style="width:225px; margin-bottom:.7em;">
					<img src="images/land/Domestic-animals-land.png" style="width:100%;"></div>			
					<p id="first">Land consumption is another troubling issue, since
it removes space that would otherwise be left as
untouched wildlife. The damage is more pronounced
for ‘free range’ livestock then conventional because
the latter are housed in more ‘efficient’ [crowded]
conditions.<br>
In the U.S. 788 million acres of land are used for
grazing livestock. Additionally about <a href="http://westernwatersheds.org/watmess/watmess_2002/2002html_summer/article6.htm"
target="_mozilla">200 million
acres</a> are used for animal feed. In South America, 70% of formerly native forests
are being clearcut in order to provide grazing land for cattle. Trees are felled at an
incredible rate to satisfy the growing market for animal meat.</p>
			</div><!-- textblock -->

			<div id="separator"></div>
			<blockquote style="margin:2em auto;">Imagine if trees gave off wifi signals, we would be planting so many trees...and
we’d probably save the planet. Too bad they only produce the oxygen that we
breathe.”</blockquote>
			
			<div class="textblock">
				<div class="image right" style="width:300px; margin-bottom:.7em;">
			<img src="images/land/Pave-paradise.jpg" style="width:100%;" class="corner-round">
			</div>
					<p>Transportation as well has a similarly massive impact
on the health of our land. In the United States, 158,000
square km. (61,000 sq. mi.) of land is devoted to roads
and parking lots. This results in paved areas consuming
between 30% and 60% of the total land surface in
North American cities. (<a href="http://people.hofstra.edu/geotrans/eng/ch6en/conc6en/ch6c1en.html"
target="_mozilla">link</a>) However the cumulative
effects of what is called the 
<a href="http://onlinelibrary.wiley.com/doi/10.1046/j.1523-1739.2000.99299.x/abstract;jsessionid=56979DB6ADEA9AF805C42DC9BC5FAC86.f04t04"
target="_mozilla">‘road-effect zone’</a> are much
greater. Noise and pollution spread quite far from the
road, as far as 1 1/2 miles farther. Harvard researcher
Richard Forman estimates that 1/5 of all land in the U.S.
is ecologically impacted by roads. This impact includes
human health, animal migration, and water movement. (<a href="http://www.eoearth.org/view/article/155768/"
target="_mozilla">link</a>)<br>
This high demand for space means that other users get squeezed out. With such large
swaths of land being negatively affected, it’s easy to see why so many environmental
groups are pushing for more <a href="http://www.biologicaldiversity.org/programs/public_lands/forests/roadless_area_conservation/"
target="_mozilla">‘roadless areas.’</a></p>
			</div> <!-- textblock -->
