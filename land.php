			<div class="textblock">
				<p>But why is it, that we devote so much physical space to
transportation? The reason is partly due to the size of the cars
themselves and partly the result of travel speed. A travelling car creates
a large space around it (mainly in front) that would be dangerous for
anyone else to use. This space can
be 1000 sq. ft. for a car travelling in town to several thousand square feet for a car on
the highway. (<a href="http://www.eoearth.org/view/article/155768/"
target="_mozilla">link</a>) This means that the average car, travelling at highway speed can
consume as much space as a small house! (<a href="http://www.eoearth.org/view/article/155768/"
target="_mozilla">link</a>)</p>
