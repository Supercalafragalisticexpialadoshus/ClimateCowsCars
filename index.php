So what exactly <b>is</b> Climate Change?  Throughout the world, people are
becoming more concerned about it, yet most people seem to be either
dis-empowered, or confused about how to create change
(hint, it isn't recycling).  To put it simply, Climate Change is a general
increase in world temperatures due to heat-trapping gases (the aptly-named
'Greenhouse Effect').  The term 'Global Warming' is less accurate because what
scientists predict is not for warming across the globe (as a recent U.S.
politician, who <a
