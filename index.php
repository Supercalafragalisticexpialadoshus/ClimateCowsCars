				<div id="separator"><h3>Prelude</h3></div>
				<div class="strong center">Which lifestyle choice does more to combat Climate Change, a 
		carfree lifestyle, or a vegan	lifestyle?</div>
		
				<div class="image right"><a href="http://www.veganstreet.com/">
					<img src="images/Introduction/carfree-vs-vegan.png" width="250px"></a>
				</div> 
				<div class="textblock"> <p id="firstfirst" style="padding-right:0.34em;">Like you, I've
		often asked myself that eternal question: How can I become even 
more of a pain in the ass, holier then thou Obsessive-Compulsive-Environmentalist]?  I was already car-free (and will 
remind you every time you think of buying gas), composted my food waste, and 
bought my food in bulk.  But
		then it occurred to me &#45; going vegan was the solution!  But 
I still had my doubts. Was this going to actually help the planet more then 
boycotting <a href="http://www.motherjones.com/blue-marble/2011/12/huge-shell-oil-spill-
nigeria" target="_mozilla">Shell</a> and <a 
href="https://en.wikipedia.org/wiki/ExxonMobil#Environmental_record"
		target="_mozilla">ExxonMobil</a>?  This is the question that I 
spent a year of research on answering.  Please read on, and explore with me how you too can make informed choices 
that will not only put your conscience at ease, but will help you to enjoy a 
healthier and happier life too.</p> </div>
					<div class="image center" style="padding: 0;max-width:600px;">
	 <a href="http://www.gocomics.com/pearlsbeforeswine/2014/12/08"
	target="_mozilla"><img src="images/Introduction/vegan-vs-cyclist.jpg" alt="Vegans and
	Cyclists comic" max-width="600px"></a><figcaption>&#169; Stephan Pastis</figcaption></div> 
				<div class="textblock">
					<p>Well, it was one thing to listen to biased opinions (I had them and I knew lots of others 
did too).  
But since I knew that my opinion was biased, I realized that I
needed to prove this by digging through actual facts.<br>
 The question wasn't a new one, mind you.
  It was originally raised back in 2006 when the UN commissioned a
report, titled '<a href="http://www.fao.org/docrep/010/a0701e/a0701e00.HTM"
target="_mozilla">Livestock's Long Shadow</a>' which stated that raising
domestic animals for food was a larger contributor to climate change then <i>all
transportation combined</i>.  But when I started really exploring the details, 
what I discovered was that the question was more complex then I ever imagined.<br>
(Spoiler alert: they both have a really big impact on the climate.) </p></div>

