in the data which made me think that the folks at the UN could be mistaken.  My
goal in writing this article therefore, is to look at the impact that both
choices have and to bring a greater understanding to each of the two camps of
passionate folks, about the huge impacts that <u>both</u> lifestyle choices have
for the living beings of the world.</p></div> 
