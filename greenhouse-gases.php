			<!-- ***** EMBODIED ENERGY ***** -->
			<div id="embodied-energy" class="strong"><u><b>Embodied Energy</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:225px; margin-bottom:.7em;">
			<a href="images/greenhousegas/Norway-LCA-auto.jpg">
			<img src="images/greenhousegas/Norway-LCA-auto.jpg" style="width:100%;"></a>
			<br><p style="margin:0;padding:0;text-transform:none;"><a href="http://users.ox.ac.uk/~geog0213/" target="_mozilla">Strømman et. al.</a>
Norwegian Univ. of Science and Technology</p>
			</div>
				<p>So going beyond tailpipes and cow burps, where is
the rest of the damage coming from?
Well the embodied energy for transportation
includes a long industrial process. Vehicles don’t
operate in a vacuum [no cars in outer space]. The
materials have to be mined from the ground,
manufactured, and then delivered. Estimates for the
pollution created vary widely. From as low as <a href="http://www.just-auto.com/electric-drive-technology/UK-Electric-and-hybrid-cars-are-CO2-heavy-in-production-study_n111784.aspx"
target="_mozilla">6.3
tons</a> per vehicle to as much as 30% of a car’s total
emissions. (<a href="http://www.gnesg.com/index.php?option=com_content&view=article&id=88%3Alife-cycle-assesment-in-the-automotive-industry&catid=27%3Ales-chiffres-de-lautomobile&Itemid=53"
target="_mozilla">link</a>)</p>
			<div id="separator"></div>
			
				<div class="image center" style="margin: 2em auto;">
					<img src="images/greenhousegas/lifecycle-manufacturing.jpg" alt="Life Cycle of Manufacture" width="500px">
				</div>
			</div> <!-- Textblock -->
			<div id="separator"></div>
			<div class="image right" style="width:250px; margin-bottom:.7em;">
			<img src="images/greenhousegas/Simon-cat-gas.jpg" style="width:100%;" class="corner-round">
			</div>
			<div class="textblock">
				<p>On top of the embodied energy to manufacture vehicles
there is also a great deal of embodied energy needed to
pump, process, and deliver the fuel. According to 
<a href="http://www.lowcvp.org.uk/assets/reports/Camden%20LCA%20Report%20FINAL%202005-06.pdf"
target="_mozilla">this
UK report,</a> the embodied energy for fuel is roughly equal
for that of vehicle manufacture. They estimate that the
embodied energy for both manufacture and fuel at 10%
of the vehicle’s lifetime impact. This amounts to roughly
158 million tons of CO2 worldwide. Of course the impact
depends largely on the source (offshore, tar sands, or oil
shale) and distance that the oil has to be transported.</p>

			<div id="separator"></div>
			<div class="image right" style="width:275px; margin-bottom:.7em;">
			<img src="images/greenhousegas/Vast-empty-road.jpg" style="width:100%;">
			</div>
			<div class="textblock">
				<p>Lastly we will look at the Greenhouse Gases released
for roads. According to <a href="http://www.nrmca.org/taskforce/Item_2_TalkingPoints/Sustainability/Sustainability/AthenaReport.pdf"
target="_mozilla">this Canadian study,</a> the
average carbon emissions for roads comes out to
11 metric Tonnes per sq. km. over a 50 year period.
Translating that into a world total gives us almost 35
million tonnes of greenhouse gas released per year
around the world.</p>
