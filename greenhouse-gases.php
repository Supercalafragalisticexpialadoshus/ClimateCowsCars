				<div id="separator"><h3>Greenhouse Gases</h3></div>
				<div class="textblock">
					<p id="first">Warning! There’s lots of data here, and the numbers are astronomical. After all,
we’re talking about the entire planet’s atmosphere. So this should help give you
a sense of the numbers:
					<div class="center center-border" style="margin-bottom:0;">
	 <p>1 Gigaton = 1,000,000,000 metric tonnes<br>
	 The estimated weight of Mt. Everest = 3,400,000,000 metric tonnes</p></div> 
			</div><!-- textblock -->
			
				<!-- ****** CARBON DIOXIDE ****** -->
				<div id="separator"></div>
				<div id="carbon-dioxide" class="strong"><u><b>Carbon Dioxide</b></u></div>
				<div class="image right" style="width:300px; margin-bottom:.7em;">
				<blockquote class="twitter-tweet" data-lang="en"><p lang="en" dir="ltr">Huge 1GW <a href="https://twitter.com/hashtag/Solar?src=hash">#Solar</a> Park Set To Help Pump More Oil (!) In <a href="https://twitter.com/hashtag/Oman?src=hash">#Oman</a><a href="https://t.co/yG9NlojvbY">https://t.co/yG9NlojvbY</a> <a href="https://twitter.com/hashtag/climate?src=hash">#climate</a> <a href="https://twitter.com/hashtag/irony?src=hash">#irony</a> <a href="https://t.co/81Y2gWsReI">pic.twitter.com/81Y2gWsReI</a></p>&mdash; Assaad Razzouk (@AssaadRazzouk) <a href="https://twitter.com/AssaadRazzouk/status/658316884358602752">October 25, 2015</a></blockquote>
<script async src="//platform.twitter.com/widgets.js" charset="utf-8"></script>
				</div>
				<div class="textblock">
					<p>Carbon Dioxide (CO<sup>2</sup>) has become the ruler by which
all greenhouse gases are compared. It’s mainly
produced by the world’s transportation systems,
which send up roughly 6.8 gigatons of Carbon
Dioxide (that’s 2 Mt Everests) <i>every year</i>. (<a href="http://www.internationaltransportforum.org/Pub/pdf/10GHGTrends.pdf"
target="_mozilla">link</a>)
Carbon Dioxide is released through burning coal,
oil, wood, and through deforestation. Similarly, the
raising of livestock contributes to this figure mainly
in the clearing of forests to graze animals, and in
the use of fuels for transporting animals, feed, and
supplies. (<a href="http://www.annualreviews.org/doi/abs/10.1146/annurev-environ-020411-130608"
target="_mozilla">link</a>)</p>
					<p>In terms of cost, I invite you to consider that <u>each ton</u> of CO<sup>2</sup> added to our atmosphere
causes an estimated 
<a href="http://news.stanford.edu/news/2015/january/emissions-social-costs-011215.html" target="_mozilla">$220 in economic damage</a> (I will let you, dear reader, examine the
cost of almost 7 gigatons).
					</p>
			</div><!-- textblock -->	
			<div id="separator"></div>
					<blockquote style="margin:2em auto;">You can’t spell ‘carbon,’ without CAR.”<br> 
			<i>Jeff Speck - 'Walkable City'</i></blockquote>
			
			<!-- ***** METHANE ***** -->
			<div id="methane" class="strong"><u><b>Methane</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:250px; margin-bottom:.7em;">
			<img src="images/greenhousegas/bestie-cowfart.jpg" style="width:100%;" class="corner-round">
			</div>
					<p>While released in smaller amounts, methane is all the
more troublesome since it has <a href="https://www.ipcc.ch/publications_and_data/ar4/wg1/en/ch2s2-10-2.html"
target="_mozilla">72 times</a> the impact on
the climate. For transportation, methane is released
mainly in oil drilling, through ‘flaring’ or through
pipeline leaks. (<a href="http://www.globalmethane.org/documents/oil-gas_fs_eng.pdf"
target="_mozilla">link</a>) About 14% of the world’s
methane is released this way.</p>
					<p>Grazing animals, on the other hand produce most
of the world’s methane. The combination of ‘cow
burps’ with animal waste lagoons and animal feed
results in roughly 15 gigatons of CO<sup>2</sup> equivalent per year. 
(<a href="https://www.globalmethane.org/documents/analysis_fs_en.pdf"
target="_mozilla">link</a>)
That said, I want to make an important additional point. By comparison, rice
production (the main plant-based source) contributes about 2.7 gigatons CO<sup>2</sup> equivalent
per year. (<a href="https://www.globalmethane.org/expo-docs/india10/postexpo/ag_sumfleth.pdf"
target="_mozilla">link</a>) I include this to point out that there are plant-based sources which have
a big impact as well. According to Dr. Masanobu Fukuoka, a well-known permaculture
farmer in Japan, rice farming can be done <a href="http://www.popscreen.com/v/5Ylq7/Masanobu-Fukuoka-rice-and-orchard-techniques"
target="_mozilla">less impactfully</a> by reducing the amount of
time that rice fields are flooded.</p>
			</div> <!-- textblock -->
			
			<!-- ***** NITROUS OXIDE ***** -->
			<div id="nitrous-oxide" class="strong"><u><b>Nitrous Oxide</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:250px; margin-bottom:.7em;">
			<img src="images/greenhousegas/Polar-bear-icemelt.jpg" style="width:100%;" class="corner-round">
			</div>
				<p>The third climate changing gas is Nitrous oxide.
This potent gas is 300 times as damaging as CO<sup>2</sup>.
So even though it is released in comparably small
amounts, it still packs a big punch. Nitrous oxide
is mainly produced by agriculture, with livestock
contributing 1.5 megatons of CO 2 equivalent each year.
(<a href="http://www.eoearth.org/view/article/154865/"
target="_mozilla">link</a>) By comparison Nitrous oxide emissions from
transportation are almost nil.</p>
			</div> <!-- Textblock -->
			
			<div id="separator"></div>
			<!-- ***** COMBINED IMPACT ***** -->
			<div id="combined" class="strong"><u><b>Combined Impact</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:225px; margin-bottom:.7em;">
			<img src="images/greenhousegas/clippy-meme.png" style="width:100%;" class="corner-round">
			</div>
				<p>Even with the wealth of information available though,
the issue is not as clearcut as it seemed. The world’s
transportation vehicles do produce almost 7 gigatons of
CO<sup>2</sup>. But there’s more to the story then that.</p>
				<p>The key element, which Dr. Mitloehner had said was
missing from the UN study, was something called
<a href="https://en.wiki2.org/wiki/Embodied_energy"
target="_mozilla">‘embodied energy.’</a> This is the amount of energy that
goes into something before it arrives on a store shelf. The
UN study did include the embodied energy for livestock
(land use change), but did not include the embodied
energy needed to bring a car and it’s fuel to the customer.
That’s why, in my research I looked into the whole
process, to offer an ‘apples-to-apples’ comparison. It’s difficult to make comparisons
between a living being like a cow and an automobile, but bear with me.</p>
			</div> <!-- Textblock -->

