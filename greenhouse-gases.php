			<div class="textblock">
				<p>Though the climate emissions from cars and their infrastructure
is enormous, the data nevertheless was showing that the world’s
domestic animals produce <u>more climate changing gases then all of
the world’s transportation.</u> But wait, there’s even more to this story.</p>
			</div><!-- Textblock -->
			<div id="separator"></div>
			
			<div class="textblock">
				<p>Dr. David Steele of <a href="http://www.earthsave.ca/"
target="_mozilla">EarthSave Canada</a> wrote an 
<a href="http://www.vegan.com/articles/environment/a-sympathetic-but-skeptical-look-at-goodland-and-anhangs-livestock-and-climate-change/"
target="_mozilla">eloquent piece,</a> describing other factors
not included in the UN study. In this case the most significant being the Carbon
Dioxide exhaled by the millions of cows daily (just like we do). His conservative
analysis estimates that 3.2% of world emissions can be attributed to the breathing of
livestock. He also suggests an additional 2% increase due to the gas exhaled by farmed
fish, which I agree is important. Adding the sum total, we get an additional 3 gigatons
of CO2 equivalent.</p>
