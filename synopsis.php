				<div id="separator"><h3>Summary</h3></div>
				<div class="textblock">
					<p id="first">So when it's all said and done, and the hours of sifting through science journals is finished, what do we eventually find out?</p>
			</div><!-- textblock -->
			<div id="separator"></div>
			
			<div class="image right" style="width:150px; margin-bottom:.7em;">
			<a href="https://www.cartoonstock.com/cartoonview.asp?catref=bfrn385&ANDkeyword=vegan+viking&NOTkeyword=&TITLEkeyword=&categories=All+Categories&artists=146&mainArchive=mainArchive&newsCartoon=newsCartoon&vintage=vintage&colorOption1=colour&colorOption2=&orientationOption1=portrait&orientationOption2=&cp=0&limit=12" target="_mozilla">
			<img src="images/greenhousegas/Vegan-viking.jpg" style="width:100%;" class="corner-round"></a>
			<figcaption>&#169; Brain Fray</figcaption>
			</div>
			<div class="textblock">
				<p>Well, based on all of this information, I was forced to change my
beliefs and accept that in the strict sense of climate change,
a plant-based lifestyle <u>does</u> have a greater positive effect
on climate change then a car-free lifestyle. In fact animal
agriculture creates more climate-changing gases then <u>all of the
world’s transportation combined,</u> mainly from 
<a href="http://www.theguardian.com/environment/2014/jul/21/giving-up-beef-reduce-carbon-footprint-more-than-cars"
target="_mozilla">cattle.</a> Even
when air travel is considered, the total is only 9 gigatons for all
sources. Meanwhile, the shear number of livestock throughout
the world creates over 22 gigatons. (<a href="http://www.youthkiawaaz.com/2015/10/livestock-creating-greenhouse-gases/"
target="_mozilla">link</a>)</p>
			</div><!-- Textblock -->

			<div id="separator"></div>
				<div class="image center" style="margin: 2em auto;">
					<img src="images/greenhousegas/World-GHG-livestock-transport.png" alt="GHG Emissions Livestock/Transport" width="550px">
				</div>
		
			<div class="textblock">
				<div class="image right" style="width:125px; margin-bottom:.7em;">
					<a href="http://thischangeseverything.org/">
					<img src="images/greenhousegas/Klein-This-changes-everything.jpg" style="width:100%;"></a>
				</div>
				<p>After all of this analysis, does this mean people who eat a vegan diet
but drive are now off the hook? Does this mean that I’m going to run
out and get a drivers license? Of course not. As I will be repeating
throughout this essay, the climate crisis is affecting much more
then just the Earth’s temperature and we are going to have to use
every tool available in order to reduce the harm of human lifestyles.
Changing our relationship with the climate is going to become more
and more challenging the longer we delay. At the risk of sounding too
negative, it cannot be ignored that humanity has already passed the
point where mild shifts can save us. As Naomi Klein states, “there are no non-radical
options left before us.” (<a href="http://www.alternet.org/naomi-klein-climate-change-not-just-about-things-getting-hotter"
target="_mozilla">link</a>) We’re not just looking at a changing climate, we’re looking
at a changing ......everything.</p>
			</div> <!-- Textblock -->
			<div id="separator"></div>
			
			<blockquote style="margin:2em auto;">There is a vast territory between what we’re trying to leave behind, and
where we want to go - and we don’t have any maps for that territory.”<br> 
			<a href="https://www.youtube.com/watch?v=_oevXkJY-fE" target="_mozilla"><i>Charles Eisenstein</i></a></blockquote>
			<div id="separator"></div>
			
			<!-- ***** ALTERNATIVES ***** -->
			<div id="alternatives" class="strong"><u><b>Meat the Alternatives</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:250px; margin-bottom:.7em;">
			<img src="images/greenhousegas/Unhealthy-vegan.jpg" style="width:100%;" class="corner-round">
			</div>
				<p>If you will indulge me, I hope to expand the scope of this
essay to include other sources of impact beyond climate
change. For those of you who are already vegan, you
may regularly enjoy the recent plethora of ‘fake meat’
products such as Tofurkey. Unfortunately, these require
so much in terms of water and energy to create, that they
cause <a href="http://www.foodnavigator.com/Market-Trends/How-eco-friendly-are-meat-analogues"
target="_mozilla">a noticeable reduction</a> in the environmental gains
of a vegan diet. There are even some fake meat products
which are produced using the neurotoxin <a href="http://plenteousveg.com/vegan-processed-foods/"
target="_mozilla">hexane.</a></p>
				<p>Additionally, unhealthy vegan foods such as white rice and bleached flour can actually
increase the risk diabetes and other diseases. (<a href="http://www.ncbi.nlm.nih.gov/pmc/articles/PMC3024208/pdf/nihms261139.pdf"
target="_mozilla">link</a>)</p>
			</div> <!-- Textblock -->
			<div id="separator"></div>

			<div class="image right" style="width:275px; margin-bottom:.7em;">
				<img src="images/greenhousegas/Lift-ocean-plastic.png" style="width:100%;">
				<figcaption><a href="http://ferdi-rizkiyanto.blogspot.com/" target="_mozilla">&#169; Ferdi Rizkiyanto</a></figcaption>
			</div>
			<div class="textblock">
				<p>People may decide to change their diet for any number of
reasons. However if you decide to go vegan for the health
benefits, then please make informed purchases. There are
a great number of processed vegan products which offer a
smaller health benefit then might be expected.</p>
				<p>If you are transitioning to a lower meat or dairy menu and
having trouble, then these foods are a great way to help
you eat lower on the food chain. On the other hand, if you
already feel comfortable with animal-free food, then bulk
veggies and grains are the best way to go. The added plastic packaging in prepared
foods puts a big strain on our climate, so much in fact that <a href="http://robgreenfield.tv/vegan/"
target="_mozilla">Rob Greenfeild</a> suggests
that the strain on our ecology can be larger then from eating animals. The most
compassionate food choice always comes from using whole foods in their natural forms.
(<a href="http://ecologycenter.org/factsheets/plastichealtheffects/"
target="_mozilla">link</a>) I welcome you to explore the many resources that can help you to ditch plastic
and eat naturally. (<a href="http://www.filmsforaction.org/articles/10-life-hacks-to-help-you-cut-plastic-out-of-the-picture/"
target="_mozilla">link</a>)</p>
			</div> <!-- Textblock -->
			<div id="separator"></div>
			
			<blockquote style="margin:2em auto;">Happiness is when what you think, what you do, and what you say are in
harmony.”<br> 
			<i>Mahatma Ghandi</i></blockquote>
			
			<div class="image right" style="width:170px; margin-bottom:.7em;">
				<a href="http://andysinger.com/" target="_mozilla"><img src="images/greenhousegas/We-can-bike.jpg" style="width:100%;"></a>
				<figcaption>&#169; Andy Singer</figcaption>
			</div>
			<div class="textblock">
				<p>For some of you, this will be convincing enough to help you
make changes to your lifestyle. Others may feel that this is all
too overwhelming. Both of these reactions are perfectly okay.<br>
For those in the latter crowd, I welcome you to take small
steps. Go to <a href="link"
target="_mozilla">a tree planting event,</a> participate in <a href="link"
target="_mozilla">‘meatless Mondays,’</a> or ride a bike to the store. Every step that we
collectively take to reduce climate changing emissions is
unquestionably helpful for the well-being of humans, animals,
and our planetary ecosystems.</p>
			<div id="separator"></div>
				<p>What I hope to impress on you with this essay, is that personal
choices based on a single area of impact are merely one
chapter in a larger and more beautiful novel. If you take in the full breadth of issues
affecting the world and consider how interconnected everything is, that will help you
develop a broader and richer level of awareness. You might already be a passionate
activist, protester, or social justice advocate. You may believe that where you are right
now is good enough. But that’s not the end of the story. While most of us think about
being earth-friendly in single-issue terms; being vegan, political campaigning, ditching
plastic, etc. The truth is that there are many choices that we make every day, and all of
them have an impact either positive or not.</p>
			</div> <!-- Textblock -->

				<div class="image right" style="width:275px; margin-bottom:.7em;">
			<img src="images/greenhousegas/Roll-over-urban.jpg" style="width:100%;">
			</div>
			<div class="textblock">
				<p>As you read on, I will share with you other issues
surrounding both livestock and motorized transport
beyond their effects on climate-change alone. The
effects of both decisions are wide-ranging and often
less obvious then we would expect. This is why I
support you in continuously pushing yourself to
live more in line with our host planet. I hope that
you will use this essay as a source of inspiration to
follow in such a path. Support <a href="https://www.crueltyfreeinternational.org/"
target="_mozilla">cruelty-free products,</a>
lower your heat and wear sweaters, or ride a bike to the store for small items. Every
step is a higher evolution for our own health, the animals, and our collective planet.</p>	
			</div> <!-- Textblock -->
		
