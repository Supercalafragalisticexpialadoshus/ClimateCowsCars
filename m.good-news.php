				<p>I have heard many people actually use pollution to justify the use of their car as protection from both the
noise and the pollution of roadways. More often then not however, the environment inside of
a car is actually <u>more</u> harmful. Data from several studies point out that the high concentration of pollutants, especially 
when starting an engine and idling does extensive harm to the body. 
(<a href="http://consumer.healthday.com/environmental-health-information-12/environment-health-news-233/pollution-exposure-peaks-at-stop-lights-study-finds-696357.html" target="_mozilla">link</a>) 
