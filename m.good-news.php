				<p><a href="http://www.hcdoes.org/airquality/monitoring/pm.htm" target="_mozilla">Particulate matter</a> (PM) 
				is microscopic pollution caused mainly by road exhaust. As the amount of small particles in the
air rises, even from low levels, there is a direct link to damage to the lungs, heart and circulatory system. 
This can bring an increased risk of lung cancer for some. For others, existing diseases like bronchitis and asthma become worse. 
People living in areas with high levels of particulate matter are believed to have a greatly reduced life expectancy. 
(<a href="http://www.ncbi.nlm.nih.gov/pubmed/8179653/" target="_mozilla">link</a>)</p>
				
				<p>City residents who live closer to high traffic areas are found to have poor concentration, and less reliable memory
recall then people living in cleaner air. (<a href="http://www.wsj.com/articles/SB10001424052970203733504577024000381790904" target="_mozilla">link</a>) 
This may be connected to studies showing that people who breathe in more of this pollution tend to have brains that ‘age
faster.’ (<a href="http://www.motherjones.com/environment/2015/05/air-pollution-dementia-alzheimers-brain" target="_mozilla">link</a>) 
(<a href="http://www.motherjones.com/environment/2015/06/study-air-pollution-speeds-cognitive-decline" target="_mozilla">link</a>) 
The risk from these pollutants has caused France to enact 
<a href="http://www.independent.co.uk/news/world/europe/french-government-launches-new-law-allowing-it-to-ban-half-the-private-cars-in-paris-10125695.html" target="_mozilla">tough laws</a> 
limiting the number of cars entering Paris in order to reduce the damage from pollution.</p>
				
