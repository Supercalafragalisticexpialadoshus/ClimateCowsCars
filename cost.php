			<!-- ***** TRAFFIC ***** -->
			<div id="traffic" class="strong"><u><b>Traffic</b></u></div>			
			<div class="textblock">
				<div class="image right" style="width:220px; margin-bottom:.7em;">
			<img src="images/cost/Missed-turn-lost-day.jpg" style="width:100%;" class="corner-round">
			</div>
				<p>Most drivers don’t realize that the dollar cost of commuting
is only a small part of the picture. The personal cost of
owning a car dwarfs all other choices even <i>before</i> looking
at the external costs.<br>
For the average person, congestion is the most obvious of these. A 
<a href="http://newsroom.aaa.com/2011/11/aaa-study-finds-costs-associated-with-traffic-crashes-are-more-than-three-times-greater-than-congestion-costs/"
target="_mozilla">report by AAA</a>
estimates the national cost of sitting in traffic at nearly $1
trillion overall or $800 per year per person. Even 
<a href="http://blogs.wsj.com/developments/2015/03/19/the-cost-of-sprawl-more-than-1-trillion-per-year-new-report-says/"
target="_mozilla">the Wall Street Journal</a> has begun to address the high cost of auto-
centric cities as a losing venture.</p>
			</div> <!-- Textblock -->
			<div id="separator"></div>
			
			<!-- ***** PARKING ***** -->
			<div id="parking" class="strong"><u><b>Parking</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:225px; margin-bottom:.7em;">
			<img src="images/cost/Percent-car-used.png" style="width:100%;">
			</div>
				<p>Among the many hidden costs of driving is the cost of
storing the vehicle when it’s not being used. According to
researcher Donald Shoup, the cost of parking spaces ranges
from $4000 for a curbside spot to $40,000 per space in a
parking structure. He further wages that the total cost of
parking in the U.S. exceeds the value of all the country’s
vehicles and perhaps the roads as well. (<a href="http://www.shoupdogg.com/publications/"
target="_mozilla">link</a>) Think about
it this way, when you go to the store, there is no cost for
parking. Yet the store owners had to pay for that lot to be
built, and the land cannot be used for anything else. The cost of that parking is slipped
in to every item that we buy. This despite the 40,000 square km (15,400 sq. mi. ) of land
devoted to parking. 
(<a href="http://iopscience.iop.org/article/10.1088/1748-9326/5/3/034001/meta;jsessionid=605AE43E666F046ACA874F62605D2F94.c3.iopscience.cld.iop.org"
target="_mozilla">link</a>)</p>
			</div> <!-- Textblock -->
			<div id="separator"></div>
			
			<!-- ***** ACCIDENTS ***** -->
			<div id="accidents" class="strong"><u><b>'Accidents'</b></u></div>
			<div class="image right" style="width:245px; margin-bottom:.7em;">
			<img src="images/cost/Sure-we-can.jpg" style="width:100%;" class="corner-round"></div>
			<div class="textblock">
				<p>All of this is dwarfed by the gigantic cost of fixing
the damage from car crashes (often labelled
<a href="http://driving.ca/auto-news/news/why-theres-no-such-thing-as-a-car-accident"
target="_mozilla">‘accidents’</a>). According to the Federal Highway
Administration, a single fatal car crash carries
a $6,000,000 price tag. AAA estimates that car
crashes in the U.S. weighed in at $300 billion for
2009. (<a href="https://newsroom.aaa.com/wp-content/uploads/2011/11/2011_AAA_CrashvCongUpd.pdf"
target="_mozilla">link</a>) It’s interesting to realize that when
people ride bicycles or walk, there is almost no need for additional traffic signs, signals
and other safety features. This adds to the dramatic cost savings for non-motorized
travel.</p>
			</div> <!-- Textblock -->

			<div id="separator"></div>

