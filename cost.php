			<!-- ***** SEE THE FOREST FOR THE STREET ***** -->
			<div id="vegan" class="strong"><u><b>Health Costs</b></u></div>
			<div class="textblock">
				<p>I have not found studies focusing specifically on the cost of a western diet, and so I
share with you the cost of the most common diseases associated with a western diet -
heart disease, osteoporosis, and cancer. It is my personal belief that a number of other
diseases are caused or worsened by a eating animal products. There’s more on that in
the next section, but the financial impact is less definitive then for the ‘big three.’</p>
			</div><!-- Textblock -->

			<div class="image right" style="width:200px; margin-bottom:.7em;">
			<a href="website" target="_mozilla">
			<img src="images/cost/Meat-expensive-medical.jpg" style="width:100%;" class="corner-round"></a>
			<figcaption>&#169; Dan Piraro</figcaption>
			</div>
			<div class="textblock">
				<p>Heart disease in the U.S. costs $444 billion and <i>accounts for 1/6 of all spending on medical care</i>
(<a href="http://www.webmd.com/healthy-aging/features/heart-disease-medical-costs"
target="_mozilla">link</a>). A single heart bypass procedure can cost $100,000 (USD)
Cancer risk is more evenly spread around the globe
and affects western and non-western countries alike.
The best estimate for cost is a 2008 study showing
$895 billion lost worldwide due to cancer. 
(<a href="http://www.cancer.org/acs/groups/content/@internationalaffairs/documents/document/acspc-026203.pdf"
target="_mozilla">link</a>)<br>
Osteoporosis, which is closely tied to the <a href="http://www.pcrm.org/health/diets/vegdiets/health-concerns-about-dairy-products"
target="_mozilla">nutrition
imbalances</a> of dairy can actually have a higher
economic impact then most cancers. For the regions
where information is available, I found that the cost
of osteoporosis on us directly or to society is <a href="http://www.iofbonehealth.org/facts-statistics"
target="_mozilla">€37
billion</a> in Europe and <a href="http://www.ncbi.nlm.nih.gov/pubmed/21165602"
target="_mozilla">$22 billion</a> in the U.S. Heart
disease, does most heavily affect smokers and/or
people who eat meat, and osteoporosis is closely linked to diet as well. Cancer, on the
other hand, is linked not only to diet but to pollution, toxic elements like asbestos, and
aluminium.<br>
I cover more on this in the next section.</p>
			</div><!-- Textblock -->
