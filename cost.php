			<div class="textblock">
				<p>On top of these enormous costs, the cost of our increasingly destructive weather due to
climate change has been, thus far, overlooked. As I mentioned previously, each ton of
CO 2 equivalent causes $220 in economic damage. This affects the cost of living, the cost
of goods, and even the cost of repairing the damage. (<a href="http://news.stanford.edu/news/2015/october/climate-change-cost-102115.html"
target="_mozilla">link</a>) (<a href="http://linkis.com/huffingtonpost.ca/EIkYt"
target="_mozilla">link</a>)<br>
So if you think that ‘hippy food’ is too expensive, or that cities shouldn’t spend tax
dollars on bike lanes, consider how much worse off we would be if nobody was taking
action.</p>		
