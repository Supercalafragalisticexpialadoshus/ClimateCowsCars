			<div class="textblock">
				<p>In 2008 the United States spent $181 billion to build and
maintain roadways. (<a href="http://www.fhwa.dot.gov/policyinformation/statistics/2008/hf2.cfm"
target="_mozilla">link</a>) But looking even further,
there is a vast amount of wealth given to petroleum
companies in the form of tax breaks. Here is a list
of what the oil companies get just in the United
States alone. (<a href="http://priceofoil.org/content/uploads/2014/07/OCI_US_FF_Subsidies_Final_Screen.pdf"
target="_mozilla">link</a>) Worldwide, these costs average
$5,300,000,000,000 or <u>$10 per minute for every human
being on the planet.</u> (<a href="http://www.theguardian.com/environment/2015/may/18/fossil-fuel-companies-getting-10m-a-minute-in-subsidies-says-imf"
target="_mozilla">link</a>)</p>
			</div><!-- Textblock -->
			<div id="separator"></div>

			<!-- ***** SEE THE FOREST FOR THE STREET ***** -->
			<div id="forest4street" class="strong"><u><b>Not Seeing the Forest for the Street</b></u></div>
			<div class="textblock">
				<div class="sidequote left" style="width:5.75em;">"Car drivers don't windowshop."
			</div>
				<p>So with all of these well documented costs and consequences, why
do we continue to see parking lots and roadway expansion? That
is a highly complex question which I will touch on superficially.
The simplest overarching answer is to consider that politicians
and planners, the people who have the most influence on society’s
transport choices rarely walk or ride a bike. These public servants
are heavily influenced by business owners which have a surprising
disconnect regarding which people create the most profit. A group
of <a href="http://www.citylab.com/cityfixer/2015/03/the-complete-business-case-for-converting-street-parking-into-bike-lanes/387595/"
target="_mozilla">12 studies</a> from around the world prove that contrary to the
beliefs of merchants, the group which spends the most at local businesses are people
who travelled by bicycle. According to 
<a href="http://bikeportland.org/2013/05/09/scenic-bikeways-help-power-oregon-bike-tourism-to-400-million-annual-impact-86531"
target="_mozilla">this study</a> for Travel Oregon, bicycle tourists
contributed $400 million to their state’s economy.<br>
				It’s easy to see how the enormous savings from bicycle travel would end up being spent
on local businesses rather then on fuel and auto repairs.</p>
			</div> <!-- Textblock -->
