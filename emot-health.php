				<p>One example of the emotional conflict that I personally see every day, is the obvious
culture of fear that exists between sidewalks. Most of us every day see pedestrians
jogging or running across the street (even if there are no cars nearby). This is considered a normal, everyday
occurrence.<br>
What most drivers fail to notice (as they speed past) is that the person tends to go back to walking once they reach the
bike lane or sidewalk. There is a very clear sense that the
road does not belong to people, but to cars. <a href="http://www.vox.com/2015/1/15/7551873/jaywalking-history"
target="_mozilla">This wasn’t always the case</a> though. Before the 1920s, people saw nothing unusual in walking in or
across the street wherever it was convenient. Business interests at the time had a <a href="https://www.youtube.com/results?search_query=adam+ruins+everything+jaywalking&spfreload=10"
target="_mozilla">well-
documented role</a> in demonizing the use of the road by non-drivers.</p>
