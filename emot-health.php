slaughterhouse. If you feel horrified by images of a slaughterhouse but not from images of people picking from fruit trees, then this is a powerful clue into what your body naturally wants.<br>
However this essay touches you, I ask only that you take a moment each day to consider
the billions of conscious animals which are killed for the sake of the human diet, and
think about whether you want to support that in your life.</p>
