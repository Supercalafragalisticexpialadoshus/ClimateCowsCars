				<p>Lastly, the issue of our current climate catastrophe
itself is causing many people to feel hopeless, depressed,
and apathetic. (<a href="https://www.apa.org/science/about/publications/climate-change-booklet.pdf"
target="_mozilla">link</a>). The emotional pain which people feel is only recently becoming connected with lifestyle
choices and climate. The effects of unstable weather, scarce resources, and other effects of climate change
are weighing more heavily on our collective sense of
security and hope. Researchers <a href="http://apy.sagepub.com/content/17/6/453" target="_mozilla">Helen Berry</a> and James
Williams have spent years researching depression and
anxiety in Australia where climate fears have been felt since the term ‘ozone hole’ first entered the public’s eye. (<a href="http://grist.org/climate-energy/what-is-climate-change-doing-to-our-mental-health/"
target="_mozilla">link</a>) Through the work of Dr. Berry and other researchers, the awareness of depression and 
<a href="https://en.wikipedia.org/wiki/Solastalgia" target="_mozilla">solastalgia</a> stemming from
a more hostile climate are finally becoming more fully understood.</p>
