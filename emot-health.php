are actually slaughtering the animals. Concentrated is a
very appropriate term as the distancing of us all from the
slaughterhouse causes a very small number of people to
experience the terror experienced by millions of animals.</p>
				<p>As humans, we are uncomfortable with seeing images of animal slaughter and that
is a good sign. It shows that “we are not predators. Predators don’t empathize with
their prey.” It shows that we are compassionate beings. Think about it this way,
