				<div id="separator"><h3>Some Good News</h3></div>
				<div class="textblock">
					<p id="first">There is good news in all of this gloom and doom. The key here is to understand
that knowledge is power. It’s not that we see different things then those who are
unaware of these issues.... But once we understand the reality of these issues on a deep
level, we simply see the same things differently.</p>
			</div><!-- textblock -->	
			<div id="separator"></div>
			<blockquote style="margin:2em auto;">It’s not that we see different things, it’s that we begin to see the same things....
differently.”<br> 
			<i>Dr. Melanie Joy</i></blockquote>
			
			<div class="textblock">
				<div class="image right" style="width:225px; margin-bottom:.7em;">
			<img src="images/goodnews/EmilyF-impossible.jpg" style="width:100%;" class="corner-round">
			<br><p style="margin:0;padding:0;text-transform:none;">Photo by Jonathan Maus of 
			<a href="http://www.bikeportland.org" target="_mozilla">BikePortland</a></p>
			</div>
					<p>We begin to understand that the decisions to choose ‘the
easy way’ or follow mainstream society run counter to our
true feelings of compassion, respect, and connection with
our living world. The idea of treating the natural world as a
‘resource to be exploited’ no longer feels conscientious. This
isn’t a feeling that most people understand on the surface,
but it exists within each of us on a deeper level, and it
reduces our sense of self-respect.</p>
					<p>The power of the human mind is now demonstrating to
us that we can use our great talents for the benefit of all,
rather then just for the sake of making a buck. But instead
of sharing news about hybrid cars, nuclear power, or other
forms of greenwashing....I’m going to share with you the
REAL solutions which have the greatest potential to help us live in harmony.<br>
(psst, and they’re either cheap or free).</p>
			<div id="separator"></div>

					<p>I’ve been amazed at the level of genius which has been showing up on the internet
in recent times. People young and old, from all over the world, are coming up with
awesome solutions to our current shared global crisis.</p>
			<div id="separator"></div>
			</div> <!-- textblock -->
			
			<!-- ***** PEOPLE DOING AWESOME SH*T ***** -->
			<div class="textblock left-indent">
				<li><a href="http://williamkamkwamba.typepad.com/"
target="_mozilla">William Kamkwamba</a> was a 15 year old boy from Malawi who became famous for building a
wind turbine from scrap parts to provide electricity to his village.</li>
				<li>In 1979 <a href="http://timesofindia.indiatimes.com/home/environment/developmental-issues/30-year-journey-from-tribal-boy-to-Forest-Man/articleshow/39510215.cms"
target="_mozilla">Jadav Payeng</a> started growing bamboo on a parched island near Bangalore. He devoted
30 years to the project and has now built a dense forest covering over 550 hectares (1300 acres).<br>
Of course most of us aren’t going to spend that much time, but groups like 
<a href="http://www.friendsoftrees.org" target="_mozilla">Friends of Trees</a> can
help you find ways to plant trees around the neighbourhood.</li>
				<li><a href="http://foodnotbombs.net/"
target="_mozilla">Food Not Bombs</a> is an international group of people who take food that would otherwise be
•	 discarded, and turn it into meals that they serve to the public for free.</li>
				<li>Lauren Singer decided in 2011 to cut out the wasteful production of trash from her life. Since
then she’s not contributed a single piece of trash. Read more about her story <a href="http://www.trashisfortossers.com"
target="_mozilla">here.</a></li>
				<li>A lot of smart people have figured out how to build their own solar panel for super cheap. 
				<a href="http://www.instructables.com/id/How-to-Get-Cheap-Solar-Power/"
target="_mozilla">Here</a> is an instructables on how.</li>
				<li>Want to heat your house for free? No I’m not selling anything. But there is a thing, called
passive solar heat. It’s actually super easy to make a solar heater out of soda cans. You can look
up ‘pop can solar heater’ on youtube, but here’s the <a href="https://www.youtube.com/watch?v=XsF9RvVxFc4"
target="_mozilla">quickest way</a> that I’ve found.</li>
				<li>Composting is a great way to turn food waste into healthy soil. If you don’t have a yard or a lot
of space, a worm bin is an easy way to do compost right under the kitchen sink. <a href="https://www.youtube.com/watch?v=CWktQVIkTDI"
target="_mozilla">This woman</a> shows you how.</li>
			Notice, these ideas don’t involve spending a week’s paycheck at the store.
