				<p>Beyond taking steps like this in our own life, we can
share the understanding and be a positive influence for
change with other people. Popular films like <a href="link"
target="_mozilla">‘Forks
over Knives’</a> and <a href="link"
target="_mozilla">‘Cowspiracy’</a> (which is on Netflix)
are wonderful tools to help us raise awareness for
animal-free diets. Influential public figures like sports
heroes, politicians, actors and actresses are embracing
veganism and using their positions to be positive role-
models. As the picture here shows, each one of us that
makes a change causes friends, coworkers, and family
to re-think their own choices and consider the benefits
of compassionate lives.</p>
