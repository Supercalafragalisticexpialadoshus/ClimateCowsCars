			<div class="textblock">
				<p>Within the meat industry, similar 'hidden costs' exist at slaughterhouses. Currently, meatpacking is most dangerous job in the U.S. (<a href="http://www.motherjones.com/politics/2001/07/dangerous-meatpacking-jobs-eric-schlosser"
target="_mozilla">link</a>)
The meatpacking industry not only has the highest injury rate, but also has by far the highest rate of serious injury - more than five times the national average. Officially, about 40,000 meatpacking workers are injured on the job every year (but the industry regularly downplays these figures). (<a href="https://www.hrw.org/news/2005/08/02/meatpackings-human-toll"
target="_mozilla">link</a>) </p>
		
