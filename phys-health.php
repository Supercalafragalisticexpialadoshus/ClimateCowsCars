				<p>Beyond the direct health costs involved in driving
every day, are the enormous costs borne by the
people who obtain the resources in the first place.
Conditions at oil rigs in the Midwest of the U.S.
have recently exposed a long-running human-rights
disaster there. The blatant disregard for safety and
ecological risk is nothing new, as activists abroad
have long known. (<a href="link"
