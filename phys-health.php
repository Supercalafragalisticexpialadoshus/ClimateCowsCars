				<p>In the vegan community I hear a strong outcry not only
against animal agriculture, but to the <a href="http://www.veganpeace.com/animal_cruelty/wool.htm"
target="_mozilla">many other ways</a>
in which animal are harmed. Every year scientists kill
an estimated 100 million lab animals for <a href="http://www.humanesociety.org/issues/biomedical_research/qa/questions_answers.html"
target="_mozilla">‘research’</a> and <a href="http://www.ocpausa.org/hunting.htm"
target="_mozilla">hunters</a> kill 200 million game animals. However even
I was surprised to learn that drivers kill nearly 400
million animals per year on the road. (<a href="http://www.culturechange.org/issue8/roadkill.htm"
target="_mozilla">link</a>)
So, while it’s clear that the meat industry is the highest
cause of intentional harm to animals, America’s motorists are the largest cause of
purposeless harm.</p>
