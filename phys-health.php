			<div id="other" class="strong"><u><b>Other Diseases</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:220px; margin-bottom:.7em;">
			<img src="images/phys-health/Dark-side-moo.jpg" style="width:100%;" class="corner-round">
			</div>
				<p>Beyond the most common lifestyle diseases, there are also
a host of other diseases such as <a href="http://nutritionfacts.org/video/preventing-strokes-with-diet/"
target="_mozilla">Stroke,</a> <a href="http://www.overcomingmultiplesclerosis.org/About-MS/Causes-of-MS/Cows-Milk-Connection/"
target="_mozilla">Multiple Sclerosis,</a>
<a href="http://www.diabeticlivingonline.com/food-to-eat/nutrition/should-you-go-vegetarian-benefits-plant-based-diet-people-diabetes"
target="_mozilla">Diabetes,</a> <a href="http://www.pcrm.org/health/health-topics/foods-and-arthritis"
target="_mozilla">Arthritis,</a> Parkinsons Disease, and <a href="http://www.ncbi.nlm.nih.gov/pubmed/23239205"
target="_mozilla">Alzheimer’s
Disease</a> which have different amounts of evidence
connecting them with diet. Vegetarians too, should be
wary of the data coming in on animal products. Many
of the same health problems can be caused (to a lesser
degree) by eggs and dairy. 
<a href="https://www.washingtonpost.com/news/to-your-health/wp/2014/10/31/study-milk-may-not-be-very-good-for-bones-or-the-body/"
target="_mozilla">Osteoporosis</a> and poor bone
health are linked to higher consumption of milk (which I
covered in the previous section). Meanwhile, activist Emily
Barwick created an <a href="https://www.youtube.com/watch?v=IsgDwSvkJdM"
target="_mozilla">in-depth video</a> describing the horrific
health consequences of the egg industry. Additionally, 
<a href="http://health.usnews.com/health-news/blogs/eat-run/2012/11/30/is-dairy-healthy-or-not"
target="_mozilla">this
article</a> offers an unbiased view on how dairy affects our health.</p>
				<p>There’s some disagreement in the research as to whether animal products are the main
cause or just a contributing factor. This depends on which disease, how much meat is
consumed relative to whole grains, and the amount of exercise a person gets. What IS
clear, is that ever increasing numbers of health professionals are speaking out about the
benefits of a meat-free diet as part of a longer and more healthy life.</p>
