				<div id="separator"><h3>Physical Health</h3></div>
				<div class="textblock">
				<div class="image right" style="width:250px; margin-bottom:.7em;">
					<img src="images/phys-health/Heart-disease.jpg" style="width:100%;" class="corner-round">
				</div>
					<p id="first">While the cost of medical expenses in western
countries creates a huge burden, those costs pale
in comparison to the ultimate cost - life itself. Knowing
that these deadly diseases are completely preventable
makes the situation all the more sorrowful. Despite the
perception that the diseases common to western culture
are ‘genetic’ the question you should ask yourself is...why
do they seem concentrated not in people with similar
genetics, but similar lifestyles? Hundreds of scientific
studies around the world link unhealthy lifestyle choices like consumption of animal
products with disease. (<a href="http://www.ncbi.nlm.nih.gov/pubmed/18697819"
target="_mozilla">link</a>) (<a href="http://www.ncbi.nlm.nih.gov/pubmed/15364185"
target="_mozilla">link</a>) (<a href="http://www.ornishspectrum.com/wp-content/uploads/Intensive-lifestyle-changes-for-reversal-of-coronary-heart-disease1.pdf"
target="_mozilla">link</a>) Yet strangely, doctors and medical experts
rarely tell us about healthy nutrition. This is because, like carfree lifestyles, there is
less profit in preventing sickness. We’ve all heard commercials telling us to ‘ask your
doctor if ‘X’ drug is right for you.’ How many of us though, have heard a commercial
applauding the medical benefits of lentils? Sadly, the medical doctors and experts
that we trust to learn about health, actually have <a href="link"
target="_mozilla">next to zero</a> required coursework in
nutritional health. It’s therefore up to us to understand this relationship, with the help
of people like Dr. Michael Greger.</p>
			</div><!-- textblock -->
			
			<blockquote style="margin:2em auto;">A doctor a day keeps the apple away”</blockquote>				
			<div id="separator"></div>
			
			<!-- ***** HEART DISEASE ***** -->
			<div id="heart-disease" class="strong"><u><b>Heart Disease</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:200px; margin-bottom:.7em;">
			<img src="images/phys-health/Vegan-good4heart.jpg" style="width:100%;">
			</div>
					<p>As I mentioned in the previous section, a huge number
of the most harmful diseases are linked with animal
products. According to the World Health Organization,
roughly 17 million people around the world are killed
each year by heart disease. 
(<a href="http://www.bellevuecollege.edu/artshum/materials/inter/Spring04/SizeMatters/internatCardioDisSTATsp04.pdf"
target="_mozilla">link</a>) This makes it the
leading cause of death in developing countries for middle-
age and older adults. Despite enormous time and effort
into studying the disease, most professionals ignore the
connection to lifestyle and assume that these diseases are
genetic. The simplest rebuttal to this is to look at studies
of groups who have immigrated from areas with healthy
diets and adopted western lifestyles. These people consistently experience degraded
health and energy levels as their diet becomes filled with sugar, meat, and cholesterol.
This is described as ‘dietary acculturation.’
(<a href="http://www.ncbi.nlm.nih.gov/pmc/articles/PMC3928252/"
target="_mozilla">link</a>)</p>
			</div> <!-- textblock -->
			<div id="separator"></div>
			
			<!-- ***** CANCER ***** -->
			<div id="cancer" class="strong"><u><b>Cancer</b></u></div>
			<div class="image right" style="width:250px; margin-bottom:.7em;">
				<img src="images/phys-health/Austin-powers-veggies.jpg" style="width:100%;" class="corner-round">
			</div>
			<div class="textblock">
				<p>The second disease most connected with lifestyle
choices is cancer. There is by now a very clear link
between many types of cancer and a person’s diet. (<a href="http://www.sciencedirect.com/science/article/pii/S0308814608006419"
target="_mozilla">link</a>)
According to the <a href="http://www.wcrf.org/int/link-between-lifestyle-cancer-risk"
target="_mozilla">World Cancer Research Fund,</a> “about
a third of the most common cancers can be prevented
through diet, maintaining a healthy weight and regular
physical activity.” The evidence links lifestyle and diet
with prostate, breast, stomach and colon, colorectal, and
pancreatic cancer. 
(<a href="http://www.medpagetoday.com/PrimaryCare/DietNutrition/46250"
target="_mozilla">link</a>)<br>
				On the other hand, it’s important to remember that the single most deadly form of
cancer is lung cancer which is caused by industrial pollution, cigarettes, and the burning
of fossil fuels. (<a href="http://www.cancerresearchuk.org/cancer-info/cancerstats/world/mortality/#Common"
target="_mozilla">link</a>)<br>
				To add another layer, several studies have linked traffic noise and stress with 
				<a href="http://www.nhs.uk/news/2012/06june/Pages/road-traffic-noise-heart-attack-mi-link.aspx"
target="_mozilla">heart attacks,</a> and <a href="http://usatoday30.usatoday.com/news/health/painter/2010-08-09-yourhealth09_ST_N.htm"
target="_mozilla">circulatory disease.</a> There is a very large body of evidence linking the
pollution from traffic-filled roads with cancer in both 
<a href="http://www.medicaldaily.com/living-near-traffic-pollution-increases-risk-cancer-children-244976"
target="_mozilla">children</a> and <a href="http://www.eltis.org/discover/news/traffic-pollution-raises-risk-cancer-study-says"
target="_mozilla">adults.</a> This brings
up one of the main reasons for this site. While some of you might eat a vegan diet, and
others might be car-light or (hopefully) car-free, only a very small number are both. I
hope that this information will help you to see the value in both choices.</p>
			</div> <!-- Textblock -->

			<blockquote style="margin:2em auto;">Nothing will benefit human health and increase chances for survival of life on Earth
as much as the evolution to a vegetarian diet.”<br>
			<i>Albert Einstein</i></blockquote>				
