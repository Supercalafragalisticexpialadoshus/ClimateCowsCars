				<p>Ironically, of the people who I’ve spoken with, the minority who are most aware of the
huge responsibility to be careful are those who regularly travel by bicycle. In my own
experience, the vulnerability of travelling without a reinforced steel safety cage teaches
us to be more attentive and avoid <a href="http://driving.ca/cadillac/cts/auto-news/news/motor-mouth-think-hands-free-is-safest-think-again"
target="_mozilla">distractions.</a><br>
In addition, <a href="https://grist.files.wordpress.com/2007/10/1621.pdf"
target="_mozilla">this study</a> from the university of Copenhagen found that the physical
activity associated with bicycle transportation reduces a person’s chance of death by
40%.</p>
