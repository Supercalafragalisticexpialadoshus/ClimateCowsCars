			<!-- ***** CARnage ***** -->
			<div id="CARnage" class="strong"><u><b>CARnage</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:260px; margin-bottom:.7em;">
			<img src="images/phys-health/From-my-cold-hands.jpg" style="width:100%;" class="corner-round">
			</div>
				<p>In addition to the damage caused by diet, car
transportation takes it’s toll as well. Worldwide
roughly 1 million people are killed in car crashes per
year, and in the United States roughly <a href="http://www.who.int/violence_injury_prevention/road_traffic/en/"
target="_mozilla">35,000 people
are killed,</a> making it the highest cause of death
for people aged 35 and under (by contrast firearms
killed 33,600). The World Health Organization states
that internationally, <a href="http://www.who.int/violence_injury_prevention/road_traffic/en/"
target="_mozilla">“tens of millions of people are
injured or disabled on the roads each year.”</a> It’s with blatant irony therefore, that I
hear people regularly speak of the fear they have with travelling outside of a car. This
belief is partly due to a number of issues including social pressure, poor awareness, and
a heavily biased transportation system.</p>
				<p>So why is it that so few people take steps to reduce this horrific CARnage? Well the
answer to that is quite simple - it’s always the other person’s fault.</p>
				<p>As humans we tend to see ourselves as more skilled then the average person without
any outside proof. One study found that 673 out of 909 motorists believed that they
were better than the average driver. (<a href="http://www.ncbi.nlm.nih.gov/pubmed/14733982/"
target="_mozilla">link</a>)<br>
But obviously we can’t ALL be better then average. So what allows people to have such
a contradictory view? Well the answer turns out to be pretty complex. You can read the
<a href="http://www.ncbi.nlm.nih.gov/pmc/articles/PMC3835346/"
target="_mozilla">full study</a> or the more abridged article 
<a href="http://www.psychologicalscience.org/index.php/news/motr/when-it-comes-to-driving-most-people-think-their-skills-are-above-average.html"
target="_mozilla">here</a>.<br>
To put it briefly, there’s no universal definition for a ‘good driver.’ Some think that
being able to balance a phone conversation with traffic makes them ‘good’ while others
might conveniently forget their own speeding only to focus on someone else’s.</p>
