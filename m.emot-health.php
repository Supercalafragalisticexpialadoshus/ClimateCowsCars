				a ‘humane’ alternative. Unfortunately, my research tells a different story. The ranchers who
market these alternative food sources may try their best, but the impacts have many of
the same harmful consequences as factory farms.
<a href="http://www.onegreenplanet.org/animalsandnature/america-land-cattle-grazing%20/" target="_mozilla">One Green Planet</a> 
