				the horrific images of mutilated cows and pigs, or baby chicks being ground up. My belief is that you can find that if 
				you want to. But many people have learned the hard way, that shock images tend to create barriers rather then break them
down. I ask you simply to consider this:</p>
				<p class="emphasis quote ui-corner-all">"If it’s not good enough for your eyes, <br>why is it good enough
for your stomach?”<br>
Emily <a href="https://www.youtube.com/user/BiteSizeVegan" target="_mozilla">Bite Sized Vegan</a></p>

				<p>Compassionate people (and I think that all of us have a sense of compassion) will fall back on the defence that 
				using animal products which are ‘free range,’ 
				<a href="http://www.motherjones.com/blue-marble/2016/02/corporations-are-going-cage-free-whats-next-hens" 
