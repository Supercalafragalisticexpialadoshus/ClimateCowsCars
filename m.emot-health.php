				<p>The emotional effects connected to a meat-based diet are more subconscious. As children growing up, our natural
tendency is to care for animals and to develop emotional bonds with human as well as non-human creatures. Because
of this, people in western society are subjected to hundreds of messages to convince us that it’s only acceptable to experience
these emotions with dogs and cats but not for dozens of other creatures. 
<a href="https://www.psychologytoday.com/experts/melanie-joy-phd" target="_mozilla">Dr. Melanie Joy</a> expresses these points with great
success in <a href="http://www.carnism.org/resources/why-we-love-dogs" target="_mozilla">her book</a> and the TED talk which 
