				<a href="http://johnrobbins.info/other-books-by-john/may-all-be-fed/" target="_mozilla">‘Diet for a New World,’</a> and 
				Dr. Will Tuttle author of <a href="http://www.worldpeacediet.com/" target="_mozilla">‘The World Peace Diet.’</a>
Riane Eisler further explains that the culture we live in is built around domination of
animals and ‘resources.’ It requires a deadening of ‘soft emotions’ like compassion
which would interfere with acts such as confinement and branding.
</p>

				<p><a  href="#google-eye" data-rel="popup" data-position-to="window" 
