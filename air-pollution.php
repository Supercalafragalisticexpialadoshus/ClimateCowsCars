			target="_mozilla"><i>Bill Adams</i></a></blockquote>

				<p>I included this section for you, dear reader, to point out that there are more issues
facing us then just greenhouse gases. Are the effects of greenhouse gases less harmful
or more harmful then the pollution in our cities? To those who suffer the effects of
these pollutants, they are both hugely important. What I encourage you to take away
from this essay, is that making choices which cause less impact benefits everyone. Not
just you yourself, but your children, your parents, your friends, your spouse...everyone
wins with less pollution and healthier lifestyle choices. This is what makes me feel
great about enjoying both a car-free lifestyle and a vegan diet.</p>	
