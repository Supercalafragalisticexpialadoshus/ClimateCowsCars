			<!-- ***** DRIVING ON FUMES ***** -->
			<div id="cabin-pollution" class="strong"><u><b>Driving On Fumes</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:240px; margin-bottom:.7em;">
			<img src="images/air-pollution/Gas-mask-car.jpg" style="width:100%;">
			</div>
				<p>I have heard many people actually use pollution to justify
the use of their automobile as protection from both the
noise and the smell of roadways in the belief that the car’s
shell will keep them safe. While there are some limited
benefits (such as being able to escape a polluted area
quickly), more often then not the environment inside of
a car is actually more harmful. Data from several studies
point out that the high concentration of pollutants, especially when starting an engine
and idling does extensive harm to the body. 
(<a href="http://consumer.healthday.com/environmental-health-information-12/environment-health-news-233/pollution-exposure-peaks-at-stop-lights-study-finds-696357.html"
target="_mozilla">link</a>) (<a href="http://www.bmj.com/content/343/bmj.d5306"
target="_mozilla">link</a>)</p>
				<p>In <a href="http://www.nzta.govt.nz/resources/research/reports/457/docs/457.pdf"
target="_mozilla">one New Zealand study,</a> it was found that drivers are exposed to 60% more Carbon
Monoxide then cyclists. Meanwhile definitive research has shown that the physical
exercise from traveling by bicycle far outweighs the danger from inhaled pollution
(read more <a href="http://www.ncbi.nlm.nih.gov/pmc/articles/PMC2920084/"
target="_mozilla">here</a>). As this and <a href="http://sjp.sagepub.com/content/40/3/245.abstract"
target="_mozilla">other studies</a> show, the more people choose active
transportation, the less pollution will be thrown into the air, and the 
<a href="http://bikeportland.org/2010/10/28/study-cycle-tracks-mean-better-air-quality-for-bikers-walkers-41754"
target="_mozilla">better we can all breathe.</a></p>
