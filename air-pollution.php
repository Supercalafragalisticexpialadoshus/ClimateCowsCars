				<div id="separator"><h3>Air Pollutants</h3></div>
				<div class="textblock">
				<div class="image right" style="width:250px; margin-bottom:.7em;">
					<img src="images/air-pollution/Eco-hold-breath.jpg" style="width:100%;">
				</div>
					<p id="first">Both unhealthy diet choices and motor vehicles
are responsible for an enormous number
of health problems in the western world (<a href="link"
target="_mozilla">link</a>).
Looking beyond the climate, we see a variety of
impacts from other gases, which cause health
problems for all of us. Like climate-changing
gases, these cause harm throughout the various
populations of our planet. The larger source of
pollution, not surprisingly, is from autos and other
combustion vehicles. <a href="link"
target="_mozilla">One study</a> found that 50,000 U.S. deaths can be attributed to
roadway pollution, while <a href="link"
target="_mozilla">another estimates over 200,000.</a> I’ll touch on just a few of the
specific gases below, but you can see a larger list <a href="link"
target="_mozilla">here.</a></p>
			</div><!-- textblock -->	
			<div id="separator"></div>
			
			<!-- ***** CARBON MONOXIDE ***** -->
			<div id="carbon-monoxide" class="strong"><u><b>Carbon Monoxide</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:150px; margin-bottom:.7em;">
			<img src="images/air-pollution/Bane-exhaust.jpg" style="width:100%;" class="corner-round">
			</div>
					<p>This colourless and odourless gas is a product of burning
fossil fuels and interrupts our lungs’ ability to deliver oxygen
throughout the body. The two areas where people are most in
danger are poorly vented rooms with combustion appliances, and
vehicles with blocked exhaust. (<a href="http://www.atsdr.cdc.gov/phs/phs.asp?id=1146&tid=253"
target="_mozilla">link</a>) While it’s rare for someone
to be killed accidentally, lower levels can cause serious harm
over time. (<a href="http://www.autoblog.com/2012/11/28/traffic-pollution-exposure-linked-to-autism-in-young-children/"
target="_mozilla">link</a>) London has recently been making headlines in
the study of urban pollution due to it’s position as one of the
most polluted cities in Europe. This is partly caused by the fact
that 2/3 of the autos are emitting illegal levels of carbon dioxide.
(<a href="http://www.theguardian.com/commentisfree/2016/feb/04/car-fumes-air-pollution-ban-motorists?utm_content=buffer694f6&utm_medium=social&utm_source=twitter.com&utm_campaign=buffer"
target="_mozilla">link</a>)</p>

			<div id="separator"></div>
			<blockquote style="margin:2em auto;">The most common source of carbon monoxide exposure is motor
vehicle exhaust. 77% of the nationwide CO emissions are from
transportation sources.”<br> 
			<a href="http://scholarsresearchlibrary.com/aasr-vol6-iss4/AASR-2014-6-4-68-73.pdf" target="_mozilla"><i>Centre for Environmental Mgmt. & Control</i></a></blockquote>
			</div> <!-- textblock -->
			
			<!-- ***** BENZENE ***** -->
			<div id="benzene" class="strong"><u><b>Benzene</b></u></div>
			<div class="textblock">
				<p>Not many people are aware of Benzene which is a short-lived toxic gas found in car
exhaust and 2nd hand smoke. (<a href="http://scholarsresearchlibrary.com/aasr-vol6-iss4/AASR-2014-6-4-68-73.pdf"
target="_mozilla">link</a>) It’s a well-established carcinogen and even in small
amounts it can irritate the skin and eyes. Samples show that it’s found at higher levels
inside of a car then in samples taken elsewhere in a neighbourhood. (<a href="http://www.euro.who.int/__data/assets/pdf_file/0005/74732/E71922.pdf"
target="_mozilla">link</a>) In fact, the
highest levels are found at gas stations and refineries. Thankfully, the U.S. EPA has
finally taken steps to tighten restrictions on the levels allowed into the air. 
(<a href="http://earthjustice.org/news/press/2015/community-and-environmental-groups-herald-improvements-in-new-oil-refinery-pollution-standards"
target="_mozilla">link</a>)</p>
			</div> <!-- Textblock -->
			
			<!-- ***** HYDROGEN SULFIDE ***** -->
			<div id="hydrogen-sulfide" class="strong"><u><b>Hydrogen Sulfide</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:100px; margin-bottom:.7em;">
			<img src="images/air-pollution/No-bullshit.svg" style="width:100%;" class="corner-round">
			</div>
				<p><a href="link"
target="_mozilla">Hydrogen Sulfide</a> is caused by the continuously increasing population
of livestock being bred by ranchers. It’s mainly produced when
animal waste is concentrated in giant <a href="http://nasdonline.org/document/1298/d001097/beware-of-manure-pit-hazards.html"
target="_mozilla">waste lagoons</a> as a result of
factory farming. Hydrogen sulfide is harmful, even at low levels, and
causes damage to the nervous system and respiratory system. The
gas is mainly concentrated at the source, however elevated levels
have been found among the homes of people living downwind from
factory farms. (<a href="http://www.environmentalhealthnews.org/ehs/newscience/2012/10/2012-1204-swine-air-pollution-raises-blood-pressure"
target="_mozilla">link</a>)</p>
			</div> <!-- Textblock -->

			<div id="separator"></div>
			<blockquote style="margin:2em auto;">Factory farm workers routinely inhale hazardous levels of particulate matter
as well as ammonia and hydrogen sulfide gases. Individually, each of these
components is capable of causing severe health complications; however, it is their
collective effect that is most harmful....”<br> 
			<a href="http://www.foodispower.org/factory-farm-workers/" target="_mozilla">
			<i>The Food Empowerment Project</i></a></blockquote>
			
			<div id="separator"></div>
			<!-- ***** OZONE ***** -->
			<div id="ozone" class="strong"><u><b>Ozone</b></u></div>
			<div class="textblock">
				<div class="image right" style="width:225px; margin-bottom:.7em;">
			<img src="images/air-pollution/Perri-air.jpg" style="width:100%;" class="corner-round">
			<br><p style="margin:0;padding:0;text-transform:none;">Image from 'SpaceBalls' by Mel Brooks</p>
			</div>
				<p>If you grew up in a dense city (like I did) you learned early
about smog alerts. <a href="http://www3.epa.gov/ozonedesignations/faq.htm#whatisozone"
target="_mozilla">Ozone,</a> which is one component of smog,
is considered the most widespread air pollutant in urban
environments. It’s created by traffic, power plants, and
industry. Breathing high amounts causes a wide range of
health problems. (<a href="http://www.airqualityontario.com/science/pollutants/ozone.php"
target="_mozilla">link</a>) According to the American Lung
Association, nearly half of the U.S. population (147 million
people) lives in counties with unhealthy air quality. (<a href="http://thinkprogress.org/climate/2014/04/30/3432601/147-million-air-pollution-ala/"
target="_mozilla">link</a>)
The U.S. EPA estimates that reducing ground level ozone by
10 parts-per-billion could prevent 1700-5000 premature deaths
per year as well as reducing childhood asthma. (<a href="https://en.m.wikipedia.org/wiki/Air_pollution#Mortality"
target="_mozilla">link</a>)</p>
			<div id="separator"></div>
			</div> <!-- Textblock -->
			
			<div id="separator"></div>
			<!-- ***** NITROGEN DIOXIDE ***** -->
			<div id="nitrogen-dioxide" class="strong"><u><b>Nitrogen Dioxide</b></u></div>
			<div class="textblock">
				<p>It’s easy to confuse Nitrogen Dioxide with Nitrous oxide. However ‘nitro’ (sometimes
called ‘laughing gas) is only a mild toxin and unlikely to be deadly. Nitrogen Dioxide, on
the other hand, causes severe harm to the lungs. (<a href="http://www3.epa.gov/airquality/nitrogenoxides/health.html"
target="_mozilla">link</a>)
Caused mainly by burning gasoline, this colourless gas is more dangerous than anything
found in cigarettes. It can also bond with other elements in the air to form particulate
matter (see below). What makes this element so scary is that the numbers for how
much pollution is being produced by the world’s cars is wildly understated. 
(<a href="http://www.theicct.org/real-world-exhaust-emissions-modern-diesel-cars"
target="_mozilla">link</a>) The
recent news around Volkswagen’s pollution rigging has only shined a spotlight on a
<a href="http://www.transportenvironment.org/publications/mind-gap-2015"
target="_mozilla">long existing scandal.</a> Throughout Northern Europe, residents are waking up to the
severe dangers which are caused by auto exhaust. 
(<a href="http://www.theguardian.com/environment/2016/feb/05/the-truth-about-londons-air-pollution"
target="_mozilla">link</a>)</p>

			<div id="separator"></div>
			<blockquote style="margin:2em auto;">Most deadly pollution can't be seen. It can't be tasted or smelled.  
			But the effects are no less tragic because of it.”</blockquote>
			
			<!-- ***** PARTICULATE MATTER ***** -->
			<div id="pm" class="strong"><u><b>Particulate Matter</b></u></div>
			<div class="image right" style="width:150px; margin-bottom:.7em;">
			<a href="https://www.cartoonstock.com/cartoonview.asp?catref=cman210&ANDkeyword=tree&NOTkeyword=&TITLEkeyword=&categories=All+Categories&artists=333&mainArchive=mainArchive&newsCartoon=newsCartoon&vintage=vintage&colorOption1=colour&colorOption2=&orientationOption1=portrait&orientationOption2=&cp=0&limit=12" target="_mozilla">
			<img src="images/air-pollution/Oxygen-cylinder-tree.jpg" style="width:100%;" class="corner-round"></a>
			<figcaption>&#169; Chris Madden</figcaption>
			</div>
			<div class="textblock">
				<p><a href="http://www.hcdoes.org/airquality/monitoring/pm.htm"
target="_mozilla">Particulate matter</a> (PM) is microscopic pollution produced
by not only road exhaust, but also by industry, and
factory farms. As the amount of small particles in the
air rises, even from low levels, there is a direct link to
damage to the lungs, heart and circulatory system. Many
people exposed run an increased risk of cardiovascular
harm and lung cancer. For others, symptoms for existing
diseases like bronchitis and asthma become dangerously
severe. People living in areas with high concentrations of
particulate matter are believed to have a greatly reduced
life expectancy. (<a href="http://www.ncbi.nlm.nih.gov/pubmed/8179653/"
target="_mozilla">link</a>)</p>
				<p>Children endure the highest risk because their lungs are still developing, which
contributes to a host of breathing problems later in life. City residents who live closer
to high traffic areas are found to have poor concentration, and less reliable memory
recall then people living in cleaner air. (<a href="http://www.wsj.com/articles/SB10001424052970203733504577024000381790904"
target="_mozilla">link</a>) This may be connected to studies showing
that people who breathe in more of this pollution tend to have brains that ‘age
faster.’ (<a href="http://www.motherjones.com/environment/2015/05/air-pollution-dementia-alzheimers-brain"
target="_mozilla">link</a>) (<a href="http://www.motherjones.com/environment/2015/06/study-air-pollution-speeds-cognitive-decline"
target="_mozilla">link</a>) The risk from these pollutants has caused France to enact 
<a href="http://www.independent.co.uk/news/world/europe/french-government-launches-new-law-allowing-it-to-ban-half-the-private-cars-in-paris-10125695.html"
target="_mozilla">tough
laws</a> limiting the number of cars entering Paris in order to reduce the damage from
pollution.</p>
			</div><!-- Textblock -->

			<div id="separator"></div>
			<blockquote style="margin:2em auto;">[children who grow up breathing polluted air] scored more poorly on intelligence
tests and were more prone to depression, anxiety and attention problems....children
born to mothers living within 1,000 feet of a major road or freeway in Los Angeles, San
Francisco or Sacramento were twice as likely to have autism, independent of gender,
ethnicity and education level.”<br> 
			<i>Epidemiologist <a href="https://news.usc.edu/30614/Study-Points-to-Environmental-Factors-of-Autism/" 
			target="_mozilla">Heather Volk</a> at USC’s Keck School of Medicine)</i></blockquote>
		
