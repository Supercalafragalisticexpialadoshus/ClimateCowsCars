are harmful, and all exposure to tobacco smoke causes damage
within the body. There is no safe level of exposure to tobacco
smoke.” However most of the same chemicals from second-hand
smoke are also given off by autos and agriculture. According
to <a href="http://www.cancer.gov/cancertopics/factsheet/Tobacco/ETS#r1"
